package final_schedule;


public class CalBean {
	private String id;
	private String title;
	private String date;
	private String idex;
	
	public String getIdex() {
		return idex;
	}
	public void setIdex(String idex) {
		this.idex = idex;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
}
